package objetos.FiguraGeometrica;

public class FiguraGeometrica {
	private int base;
	private int altura;
	/**
	 * @param base
	 * @param altura
	 */
	public FiguraGeometrica(int base, int altura) {
		super();
		this.base = base;
		this.altura = altura;
	}
	/**
	 * @return the base
	 */
	public int getBase() {
		return base;
	}
	/**
	 * @param base the base to set
	 */
	public void setBase(int base) {
		this.base = base;
	}
	/**
	 * @return the altura
	 */
	public int getAltura() {
		return altura;
	}
	/**
	 * @param altura the altura to set
	 */
	public void setAltura(int altura) {
		this.altura = altura;
	}
	
	
}
